        private static void TestEncryptDecrypt(int rounds, string key, string plaintext, string ciphertext)
        {
            byte[] bkey = BitHelper.HexToBytes(key.Replace(" ", ""));
            byte[] bplain = BitHelper.HexToBytes(plaintext.Replace(" ", ""));
            byte[] bcipher = BitHelper.HexToBytes(ciphertext.Replace(" ", ""));

            var rc5 = new RC5(bplain.Length * 4, rounds, key.Length);

            var enc = rc5.CreateEncryptor(bkey);
            Assert.IsTrue(bcipher.SequenceEqual(enc(bplain)));

            var dec = rc5.CreateDecryptor(bkey);
            Assert.IsTrue(bplain.SequenceEqual(dec(bcipher)));
        }

        private static void TestEncryptDecryptCBC(int rounds, string key, string IV, string plaintext, string ciphertext)
        {
            byte[] bkey = BitHelper.HexToBytes(key.Replace(" ", ""));
            byte[] biv = BitHelper.HexToBytes(IV.Replace(" ", ""));
            byte[] bplain = BitHelper.HexToBytes(plaintext.Replace(" ", ""));
            byte[] bcipher = BitHelper.HexToBytes(ciphertext.Replace(" ", ""));

            var rc5 = new RC5(bplain.Length * 4, rounds, key.Length);
            var cbc = new CBC();

            var enc = cbc.CreateEncryptor(rc5, bkey);
            Assert.IsTrue(bcipher.SequenceEqual(enc.Encrypt(biv, bplain)));

            var dec = cbc.CreateDecryptor(rc5, bkey);
            Assert.IsTrue(bplain.SequenceEqual(dec.Decrypt(biv, bcipher)));
        }

        private static void TestEncryptDecrypt(string key, string plaintext, string ciphertext, string ciphertext100, string ciphertext1000)
        {
            byte[] bkey = BitHelper.HexToBytes(key.Replace(" ", ""));
            byte[] bplain = BitHelper.HexToBytes(plaintext.Replace(" ", ""));
            byte[] bcipher = BitHelper.HexToBytes(ciphertext.Replace(" ", ""));
            byte[] bcipher100 = BitHelper.HexToBytes(ciphertext100.Replace(" ", ""));
            byte[] bcipher1000 = BitHelper.HexToBytes(ciphertext1000.Replace(" ", ""));

            var rc5 = new RC5(bplain.Length * 4, 12, key.Length);

            var enc = rc5.CreateEncryptor(bkey);
            Assert.IsTrue(bcipher.SequenceEqual(enc(bplain)));

            byte[] c = bplain;
            for (int i = 0; i < 100; i++)
                c = enc(c);
            Assert.IsTrue(bcipher100.SequenceEqual(c));

            c = bplain;
            for (int i = 0; i < 1000; i++)
                c = enc(c);
            Assert.IsTrue(bcipher1000.SequenceEqual(c));

            var dec = rc5.CreateDecryptor(bkey);
            Assert.IsTrue(bplain.SequenceEqual(dec(bcipher)));

            c = bcipher100;
            for (int i = 0; i < 100; i++)
                c = dec(c);
            Assert.IsTrue(bplain.SequenceEqual(c));

            c = bcipher1000;
            for (int i = 0; i < 1000; i++)
                c = dec(c);
            Assert.IsTrue(bplain.SequenceEqual(c));
        }
