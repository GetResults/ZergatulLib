        private static void TestEncryptDecrypt(string key, string plaintext, string ciphertext, string ciphertext100, string ciphertext1000)
        {
            byte[] bkey = BitHelper.HexToBytes(key.Replace(" ", ","));
            byte[] bplain = BitHelper.HexToBytes(plaintext.Replace(" ", ","));
            byte[] bcipher = BitHelper.HexToBytes(ciphertext.Replace(" ", ","));
            byte[] bcipher100 = BitHelper.HexToBytes(ciphertext100.Replace(" ", ","));
            byte[] bcipher1000 = BitHelper.HexToBytes(ciphertext1000.Replace(" ", ","));

            var bf = new Serpent(bkey.Length);

            var enc = bf.CreateEncryptor(bkey);
            Assert.IsTrue(bcipher.SequenceEqual(enc(bplain)));

            byte[] c = bplain;
            for (int i = 0; i < 100; i++)
                c = enc(c);
            Assert.IsTrue(bcipher100.SequenceEqual(c));

            c = bplain;
            for (int i = 0; i < 1000; i++)
                c = enc(c);
            Assert.IsTrue(bcipher1000.SequenceEqual(c));

            /*var dec = bf.CreateDecryptor(bkey);
            Assert.IsTrue(bplain.SequenceEqual(dec(bcipher)));*/
        }
