        byte[] h, N, Epsilon, pBuffer, K, gResult;
        int mLength;

        protected override void Init()
        {
            h = new byte[64];
            N = new byte[64];
            Epsilon = new byte[64];
            pBuffer = new byte[64];
            K = new byte[64];
            gResult = new byte[64];
            mLength = 512;

            if (HashSize == 32)
                for (int i = 0; i < 64; i++)
                    h[i] = 1;
        }

        protected override void AddPadding()
        {
            mLength = _buffer.Count * 8;
            _buffer.Add(1);
            while (_buffer.Count < 64)
                _buffer.Add(0);
        }

        protected override void ProcessBlock()
        {
            byte[] m = _block;
            Array.Reverse(m);
            g(m, false);

            // N += |M|
            int index = 63;
            while (mLength > 0)
            {
                if (index < 0)
                    throw new InvalidOperationException();
                mLength += N[index];
                N[index] = (byte)mLength;
                mLength >>= 8;
                index--;
            }
            mLength = 512;

            // Epsilon += m
            int carry = 0;
            for (index = 63; index >= 0; index--)
            {
                carry += Epsilon[index];
                carry += m[index];
                Epsilon[index] = (byte)carry;
                carry >>= 8;
            }
        }

        private void LPS(byte[] vector)
        {
            // Transformation S
            for (int i = 0; i < vector.Length; i++)
                vector[i] = Pi[vector[i]];

            Array.Copy(vector, 0, pBuffer, 0, 64);
            // Transformation P
            for (int i = 0; i < 64; i++)
                vector[i] = pBuffer[Tau[i]];

            // Transformation L
            for (int i = 0; i < 8; i++)
            {
                ulong result = 0;
                for (int j = 0; j < 8; j++)
                    result ^= APreCalc[j][vector[(i << 3) | j]];

                // non-optimized code
                /*ulong result = 0;
                for (int j = 0; j < 64; j++)
                    if ((vector[(i << 3) | (j >> 3)] & (128 >> (j & 7))) != 0)
                        result ^= A[j];*/

                BitHelper.GetBytes(result, ByteOrder.BigEndian, vector, i << 3);
            }
        }

        private void g(byte[] m, bool zero)
        {
            if (zero)
                Array.Copy(h, 0, K, 0, 64);
            else
                for (int i = 0; i < 64; i++)
                    K[i] = (byte)(h[i] ^ N[i]);
            LPS(K);

            Array.Copy(m, 0, gResult, 0, 64);

            for (int round = 0; round < 13; round++)
            {
                // X(K, m)
                for (int i = 0; i < 64; i++)
                    gResult[i] ^= K[i];

                if (round == 12)
                    break;

                // LPSX(K, m)
                LPS(gResult);

                // K = LPS(K xor C)
                byte[] Cround = C[round];
                for (int i = 0; i < 64; i++)
                    K[i] ^= Cround[i];
                LPS(K);
            }

            for (int i = 0; i < 64; i++)
                gResult[i] = (byte)(gResult[i] ^ h[i] ^ m[i]);

            Array.Copy(gResult, 0, h, 0, 64);
        }

        protected override byte[] InternalStateToBytes()
        {
            // h = g0(h, N)
            g(N, true);

            // h = g0(h, Epsilon)
            g(Epsilon, true);

            byte[] hash = new byte[HashSize];
            Array.Copy(h, 0, hash, 0, HashSize);
            Array.Reverse(hash);
            return hash;
        }
